    case 5:
      htim2.Instance->CCR2 = value;
      break;
    case 6:
      htim2.Instance->CCR3 = value;
      break;
    case 7:
      htim2.Instance->CCR4 = value;
      break;
    case 8:
      htim3.Instance->CCR1 = value;
      break;
    case 9:
      htim3.Instance->CCR2 = value;
      break;
    case 10:
